template<typename _Kind_>
void Foreach(Iterable<_Kind_> & iter, const Callback<void,_Kind_> & callback)
{
	Iterand<_Kind_> it = iter.Forward();
	while (it)
	{
		callback(it());
		++it;
	}
}

