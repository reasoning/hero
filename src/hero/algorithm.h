template<typename _Kind_>
Superstring Filter(const Callback<bool,_Kind_> & callback, String & string)
{
	Superstring filter;
	char * c = string.Data;
	char * e = string.Data+string.Size;	
	for (;c != e;++c)
		if (callback(*c))
			filter.Append(*c);
			
	return filter;
} 

template<typename _Kind_>
Array<_Kind_> Filter(const Callback<bool,_Kind_> & callback, Iterable<_Kind_> & iterable)
{
	Array<_Kind_> filter;
	Iterator<_Kind_> it = iterable.Iterate();
	for(it.Forward();it.Has();it.Move())
		if (callback(it()))
			filter.Append(it());
	
	return Array<_Kind_>(filter,true);
} 


template<typename _Kind_>
List<_Kind_> Filter(const Callback<bool,_Kind_> & callback, List<_Kind_> & list)
{
	
	
	
	
	List<_Kind_> filter;
	Iterator<_Kind_> it = list.Iterate();
	for(it.Forward();it.Has();it.Move())
		if (((Callback<bool,_Kind_> &)callback)(it()))
			filter.Append(it());
	
	
	
	
	return List<_Kind_>(filter,true);
} 



template<typename _Return_, typename _Kind_>
Array<_Return_> Map(const Callback<_Return_,_Kind_> & callback, Iterable<_Kind_> & iterable)
{
	Array<_Return_> map;
	Iterator<_Kind_> it = iterable.Iterate();
	for(it.Forward();it.Has();it.Move())
		map.Append(((Callback<_Return_,_Kind_> &)callback)(it()));
	
	return Array<_Return_>(map,true);
}

template<typename _Return_, typename _Kind_>
_Kind_ Reduce(const Callback<_Return_,_Kind_,_Kind_> & callback, Iterable<typename Type<_Kind_>::Value> & iterable)
{
	
	
	

	
	Iterand<typename Type<_Kind_>::Value> it = iterable.Forward();
	_Kind_ value = it();
	++it;
	while (it != 0)
	{
		value = ((Callback<_Return_,_Kind_,_Kind_> &)callback)(value,it());
		++it;
	}

	return value;	
	
} 

