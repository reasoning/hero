int Aligned::PowerOfTwo(int size)
{
    return Binary::PowerOfTwo(size);
}

void* Aligned::New(int size, int alignment)
{
#ifdef HERO_PLATFORM_WINDOWS

    return _aligned_malloc(size, alignment);
#endif

#ifdef HERO_PLATFORM_POSIX

    return _mm_malloc(size, alignment);
#endif
}

void Aligned::Del(void* data)
{
#ifdef HERO_PLATFORM_WINDOWS
    _aligned_free(data);
#endif

#ifdef HERO_PLATFORM_POSIX
    _mm_free(data);
#endif
}

